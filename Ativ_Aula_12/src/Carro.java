
public class Carro extends Veiculo {
		//Atributos
		private String Chassi;
		private String Tracao;
		
		//Métodos
		public void setChassi(String chassi) {
			this.Chassi = chassi; 
		}

		public String getChassi() {
			return Chassi;
		}
		
		public String getTracao() {
			return Tracao;
		}

		public void setTracao(String Tracao) {
			this.Tracao = Tracao; 
		}
		
		
}
