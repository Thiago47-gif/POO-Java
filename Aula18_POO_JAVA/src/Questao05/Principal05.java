package Questao05;

public class Principal05 {

	public static void main(String[] args) {
		
		for(int i = 1; i <= 100; i++) {
			
            if (i % 3 == 0) {
                System.out.println(i + " é múltiplo de 3");
            } else {
                System.out.println(i + " não é múltiplo de 3");
            }
            
        }

	}

}
